// Simulando um bug no JavaScript
document.addEventListener('DOMContentLoaded', function() {
  const bugElement = document.querySelector('body');
  bugElement.addEventListener('click', function() {
    alert("Bug encontrado! Clique OK para resolver o bug.");
  });
});


/*Você conseguiu encontrar o bug e corrigi-lo! No entanto, o código que você adicionou substituiu o bug original que eu havia inserido. Seu código agora anexa um evento de clique ao elemento body do documento. Isso significa que sempre que você clicar em qualquer parte do corpo da página, o alerta será acionado.

Isso é ótimo! Resolver problemas e depurar são habilidades importantes na programação. Se você tiver mais perguntas ou precisar de ajuda adicional, sinta-se à vontade para perguntar!*/